class Song
  attr_accessor :name, :artist_name
  @@all = []

  def self.all
    @@all
  end

  def save
    self.class.all << self
  end
  
  def self.create
    song = self.new
    song.name = name
    song.artist_name = artist_name
    @@all << song
    song
  end
  
  def self.new_by_name(song_string)
    @name = song_string
    self.new
  end
end
